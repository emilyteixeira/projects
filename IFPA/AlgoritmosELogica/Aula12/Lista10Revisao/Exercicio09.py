# 9. Saúde:
# Problema: Um farmacêutico precisa calcular a dosagem de um medicamento conforme o
# peso do paciente.
# Exercício: Escreva um algoritmo que receba o peso do paciente e calcule a dosagem do
# medicamento, considerando 1mg por kg de peso.

# Minha Resolução:

# Recebe o peso do paciente
peso_paciente = float(input('Qual o peso do paciente? '))

# Calcula a dosagem do medicamento considerando 1 mg por kg de peso
dosagem = peso_paciente

print(f'A dosagem correta do medicamento será de {dosagem} mg por kg.')

